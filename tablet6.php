    	<h3>Valdosta Six</h3>
    	<blockquote>A typical record of temple offerings found at Jekha,
bearing the imprint of the temple scribe's stone seal
which was applied while the clay was still moist to
make the alteration of the records impossible. The
names of the scribe and his father, along with a
seated figure of a deity and standing figures of
priests, are found on the seal impression.</blockquote>
