<div class="translation">
    <p><strong>6.1.</strong> This document records the transfer of a garment from Ikala, presumably the well-known administrator in charge of textile production in Umma (Waetzoldt 1972: 101; Dahl 2003: 184), to a person named Ayakala on behalf of Agu (mu a-gu-še3). Although the date on this tablet falls within the tenure of Ayakala, the equally well-known governor of Umma, the Ayakala mentioned in this text is not the governor of Umma: the seal impression on the tablet clearly indicates that the Ayakala in our text is a-a-kal-la dumu er2-dingir, "Ayakala, the son of Er-dingir," whereas the governor of Umma was the son of Ur-nigar (a-a-kal-la dumu ur-nigargar).</p>

<p><strong>6.2.</strong> While the Ayakala mentioned in this text is not otherwise attested in the textual record, the Er-dingir who is mentioned as his father is probably a gudu priest whose name appears in a number of documents, presumably er2-dingir dumu lugal-sa6-ga (see, for example, the seal impression of MVN 13, 190)—only one person with the name Er-dingir seems to be active in Umma at the time when this tablet was written, but further proof would be needed for a positive identification. Er-dingir is also mentioned in OLP 8, 24, 21, a list of the staff members and religious specialists at a number of institutions in Umma: Er-dingir is the last entry in a list of two dozen individuals who are described as gudu4 dšara2 ummaki, "gudu priests of the god Šara of Umma" (rev., col. iv, lines 33-34). Note as well that the seal impression mentioned above (see MVN 13, 190) describes Er-dingir's "occupation" as "servant of the god Šara" (ARAD2 dšara2), a fitting title for a member of her priesthood.</p>
