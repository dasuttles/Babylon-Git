<div class="translation">
    <p><strong>1.1.</strong> This is an account of the transfer of one-third of a mina (ca. 170 grams) of a commodity associated with flax and the production of linen from ADU to Lanimu.</p>

<p><strong>1.2.</strong> The meaning of ša3-gu remains uncertain. It seems fairly clear that it is associated with flax and the production of linen (see Potts 1997: 117-119 for a brief overview of flax and linen production in Mesopotamia). Waetzoldt (1983: 585-586), in the context of a much more extensive discussion of linen in Mesopotamia, also notes that a secure translation of ša3-gu is still a desideratum. In SET 274, rev. col. i, line 19, ša3-gu occurs in a large garment production account in which it is found among a list of slightly unusual raw materials such as gada sag10 'high-quality linen' and siki kur-ra 'foreign wool'.</p>

<p><strong>1.3.</strong> Lanimu is found in other texts that deal with the transportation of flax/linen to the linen garment workshops in Ur (see, for instance, BIN 5, 168, in which Lanimu receives a shipment of ša3-gu bound for Ur from Dadaga, the governor of Umma, and MVN 20, 202, in which Lanimu receives ša3-gu from Dingira [dingir-ra] in Ur). Linen production seems to have been centered in Ur, capital of the Ur III empire, as we might expect due to the association of linen garments with social elites in other societies, but I do not know of any study of such an association in Mesopotamia. Although the word bala "transfer; term of office" occurs in several texts that also mention Lanimu, it remains unclear whether or not Lanimu was participating in the well-known but poorly understood bala-system. At least upon initial inspection, there is a substantial possibility that Lanimu is involved in the bala-system if only because the person from whom he receives the ša3-gu, namely ADU, occurs in a number of records bearing the designation ša3 bala-a that Sharlach (2004) lists in the appendix to her volume on the bala (Chart 2.14, pp. 289-311): SAT 2, 599 (labor), Princeton 1, 234 (reeds), UTI 3, 1603 (reeds), SANTAG 6, 278 [Koslova 278 in Sharlach] (baskets), BCT 2, 16 (sheep), and MVN 14, 289 (reeds).</p>

<p><strong>1.4.</strong> With one important exception, Lanimu never occurs in a text designated ša3 bala-a. A brief review of tablets that mention Lanimu may provide some degree of clarity however. The tablets that mention Lanimu cluster on the basis of a series of administrative terms that make mention of the city of Ur (uri5ki): six tablets dating from the 45th year of Šulgi to the first year of Amar-Suen include the phrase uri5ki-še3, "to Ur" (MVN 20, 162; Orient 16, 62 70; CTNMC 40; BIN 5, 168; SACT 2, 154; Valdosta 1); six tablets dating to roughly the same period of time include the phrase ša3 uri5ki-ma, "inside of Ur" (AR RIM 4, 2; BIN 5, 154; MVN 20, 1; SANTAG 6, 83; Ontario 2, 139; SAT 2, 654). Seven tablets that also mention Lanimu and date to roughly the same period of time include the characteristic phrase zi-ga uri5ki-ma, "expended in Ur" (CST 659a; MVN 20, 202; SNAT 297; SACT 1, 69; SACT 2, 253; Syracuse 389; TCNU 451).</p>

<p><strong>1.5.</strong> Lastly, a small number of tablets include the word bala in accounts that also mention Lanimu (bala-a uri5ki-ma [MVN 15, 35 obv. 2]; [zi]-ga bala-a ša3 nibruki u3 ša3 uri5ki-ma [BIN 5, 4 obv. 13]; bala ša3 uri5ki-ma [MVN 21, 6 obv. 3]). SANTAG 6, 38 rev. 9, seems to include the telltale phrase ša3 bala-a, "inside of the bala-system," which can be interpreted as a designation of the bala-system with some confidence, but I would like to raise the possibility that this is an error for a phrase such as the one in BIN 5, 4, above (zi-ga ša3 bala-a / uri5ki-ma for an expected zi-ga bala-a ša3 uri5ki-ma, "booked out (and) transferred in Ur"). Other than this single occurrence, the phrase ša3 bala-a seems to have been studiously avoided in Lanimu's accounts. In my view, the proper interpretation of phrases such as zi-ga bala-a remains an open question in spite of Sharlach's confidence that bala-a is to be interpreted as a noun in the genitive case ("of the bala-system") rather than a nominalized verb ("that which has been transferred"). See Sharlach 2004, 115-121 for evidence in favor of interpreting these texts as part of the bala-system.</p>
