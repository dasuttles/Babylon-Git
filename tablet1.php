    	<h3>Valdosta One</h3>
    	<blockquote>An unusually perfect clay tablet of average size, dating back to the reign of Bursin of Chaldea (c. 2350 B.C.) and containing an account of the devilvery of merchandise. The tablet was found at Jekha, site of the ruins of Umma, a city of ancient Babylonia.</blockquote>
