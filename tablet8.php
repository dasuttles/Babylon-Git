    	<h3>Valdosta Eight</h3>
    	<blockquote>A temple record, found at Jekha, sealed and dated circa 2350 B.C.</blockquote>
        </div>
       
<div class="translation">
    <p><strong>8.1.</strong>This document is a receipt for a small group of low-quality garments that are being transferred from the office of the governor (ki ensi2-ka-ta) to Ikala, the chief administrator of textile production in Umma. Since the maintenance of the laborers at the textile mill managed by Ikala was often the responsibility of ur-dšara2 dumu lugal-ušur3/4, a well-known chief administrator or archivist (ša13-dub-ba) in this period, and since Ur-šara assumes responsibility for the garments, the simplest explanation is that these were old garments that the governor returned to Ikala for the use of the weavers themselves. A number of accounts document Ur-Šara's role in distributing and maintaining labor forces for institutions such as Ikala's weaving mill (see, for example, MCS 3, 54 (BM 113005), an account of labor transferred to the account of Ikala). One aspect of managing these laborers would have been to provide garments for them as in this text, but also seen elsewhere (TCNU 607, an account that mentions 891 tug2 uš-bar garments that where debited to Ikala's account by Ur-Šara). See also SANTAG 6, 67, in which Ur-šara seals a tablet that documents the repayment of a debt (la2-ia3 su-ga) by Ikala on behalf of Ur-Iškur, a fuller (azlag2), who was presumably attached to Ikala's textile production center.</p>

