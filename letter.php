    <h3>Banks, Edgar to Richard H. Powell, letter. Alpine N.J. April 30, 1920.</h3>
        <h5>Professor R.H. Powell <br> <span style="margin-left: 8%; ">Valdosta, Ga.</span></h5>
    <p>Dear Sir:-</p>
    <p>Your letter of April 17 relative to the Babylonian tablets is at hand. I have selected ten of the tablets, those showing the different types and sizes, and representing the different periods od [sic] history from which the tablets are available, and am sending them to you by parcel post. Most of the tablets are in a perfect condition. The sun dried tablets are seldom well preserved.</p>
    <p>I have tried to form a little collection complete in itself. Enclosed is a description of the tablets, and my guarantee that they are genuine. Will you kindly examine them, and should you not care to keep them all, select those that you desire, and return the others to me as early as convenient? Since the British have occupied Mesopotamia they have made some very stringent laws regarding the antiquities, and it seems that it will no longer be possible to obtain the tablets.
 Hoping that they may reach you in good condition, and that you may find them of interest, I am,
 Sincerely yours,
 
 <p style="text-align: center;">Edgar J. Banks</p>
<hr>
<h4>A Description of ten ancient Babylonian inscribed clay tablets, described by Edgar J. Banks, Alpine, N.J.</h4>  

<p><strong>No. 1.</strong> $5.00. Found at Jokha, the ruin of the ancient city of Umma in Central Babylonia. An unusually perfect tablet of the average size containing an account of the delivery of some merchandise. The date is on the back and reads The year when Bur-Sin became the king. Bur-Sin was the king of Ur of the Chaldees about 2350 B.C., the date of the tablet.</p>
 <p><strong>No. 2.</strong> $5.00. Found at Drehem, a suburb of Nippur, where there was a receiving station for the temple of Bel. A very white burned clay tablet with a record of the animals presented to the temple for the offerings. It begins with : One lamb for the God Bel, one lamb for the Goddess Belit, presented by, ?, One lamb for the Goddess Nin-k-gal, etc. These offerings were made on the 21st day of the month. The date is in the last two lines, about 2350 B.C.</p>
 <p><strong>No. 3.</strong> $4.00. Found at Jokha. A receipt for 3 lambs and 17 kid goats, delivered on the 29th day of the month. On one edge is the numeral 30, the total number of the animals. The date is on the back about 2350 B.C.</p>
 <p><strong>No. 4.</strong> $3.00. Found at Drehem. A butchers bill for 1 she goat and 2 goats, killed for market, and delivered on the 26th day of the month. Dated about 2350 B.C.</p>
 <p><strong>No. 5.</strong> $5.00. Found at Jokha. A very rare messenger tablet with a list of provisions supplied to the temple messenger for the journey, as bread, dates, oil, wine, etc. It was his expense allowance. The messenger tablets are very highly valued, for the writing upon them is finer and better than upon tablets of any other type. The date is along one edge, about 2350 B.C.</p>
 <p><strong>No. 6.</strong> $4.00. Found at Jokha. This is a typical record of the temple offerings. After the tablet was written, and while the clay was still soft, the temple scribed rolled over the entire tablet his cylindrical stone seal, and the seal impression made it impossible to change the record. The seal impression bears the name of the scribe and of his father in raised characters, the seated figure of a deity and the standing figures of priests. The date is about 2350 B.C.</p><strong></strong>
 <p><strong>No. 7.</strong> $5.00. Found at Drehem. A temple record, sealed and dated about 2350 B.C.</p>
 <p><strong>No. 8.</strong> $3.00. Found at Jokha. A temple record, sealed and dated about 2350 B.C.</p>
<p> <strong>No. 9.</strong> $3.00. Found at Senkereh, the Biblical Elassar mentioned in Genesis 14:1. This is a sun dried first dyhasty [sic] tablet or business contract, from the age of Hammurabi, King of Babylon about 2200 B.C. He was the Hammurabi of the Bible, a contemporary of Abraham. This tablet is rare. Few tablets from this dynasty are well preserved.</p>
 <p><strong>No. 10.</strong> $3.00. Found at Babylon. This is a late or neo-Babylonian contract tablet or business document of the usual form. It is sun-dried. It is dated on the back, and the date bears the name of Nabonidus, King of Babylon from 555 to 538 B.C. He was the last king of Babylon before the fall of the city to Cyrus. The tablet was written sometime between these two dates.</p>
 <p>I guarantee each of the ten tablets described above to be genuine ancient Babylonian tablets.</p>
<p style="text-align: center;">Edgar J. Banks</p>
 
