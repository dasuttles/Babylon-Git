<div class="translation">
    <p><strong>7.1.</strong> This text is heavily sealed and will need to be re-edited once it is baked and cleaned. Nonetheless, I offer a transliteration on the basis of the available image if only so as to encourage further work on the tablet. The name in the first line on the obverse is particularly rare, having only one other occurrence in the CDLI corpus, namely PDT 2, 959, rev., col. iii, line 20. Note that the total on the left edge is quite badly damaged, so the reconstruction I offer here is somewhat tentative. I would also like to thank the anonymous referee for several improvements to the provisional transliteration offered here. The referee also notes that the seal-cap impressions on Valdosta 7 may suggest that the seal is that of Ur-Shulpa'e (cf. Hilgert 2003, 605ff.).</p>

