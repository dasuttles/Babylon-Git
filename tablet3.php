    	<h3>Valdosta Three</h3>
    	<blockquote>A receipt (c.2350 B.C.) found at Jekha for three lambs and seventeen kids delivered on the twenty-
ninth day of the month. The symbol denoting the
number thirty carved on the edge of the tablet
stands for the total number of animals sacrificed.</blockquote>
        </div>
       
<div class="translation">
    <p><strong>3.1.</strong> Here we have a receipt for a group of lambs and goats originally from Ur that were transferred from the account of Abbasaga, the head of the Drehem administration, to the bureau of Nalu, one of his deputies. Another document would have recorded the transfer of these to animals to one or more cultic centers in either Nippur or perhaps in this case Ur (see Johnson 2004 [CDLB 2004:2] for an example of such a transfer).</p>

<p><strong>3.2.</strong> Because of the somewhat unusual composition of the herd in this text and the precise dating of the tablet, this herd can be identified in another document: RA 10, 210 (BM 103413). RA 10, 210 records the deliveries of animals made to Abbasaga from the household of the divinized king Šulgi over the course of one year (Amar-Suen 4). One of the deliveries mentioned in RA 10, 210, is as follows (RA 10, 210, obv. i, lines 6-10).</p>

<p><strong>3.4.</strong> The fact that the date and the rather unusual composition of the herd coincide guarantees that the two documents are related. Clearly, Abbasaga received the herd from Ur or perhaps was responsible for accounting for them even through they remained in Ur—the terminology (ša3 uri3ki-ma, "in Ur") would seem to favor the latter interpretation. Once the herd had been "received" by Abbasaga, he turned them over to Nalu, and when Nalu assumed control of the group of animals he generated Valdosta 3 to reflect the transfer of responsibility. Hilgert has dealt with Nalu's role in the administrative of Drehem to a limited extent in several publications (Hilgert 1998: 15-16; 2003: 64-65).</p>
