        <div class="info">
    	<h3>Valdosta Two</h3>
    	<blockquote>A record of sacrificial animals presented to the priests of the temple of Bel, beginning with,
"One lamb for the god Bel, one lamb for the
goddess Bel it, presented by _____________ . . . One
lamb for the goddess Nin-k-gal ..." The tablet,
dating back to circa 2350 B.C., was found at
Drehem, a suburb of Nippur, where there was a
receiving station for the temple of Bel.</blockquote>
        </div>
       
<div class="translation">
    <p><strong>2.1.</strong> This text consists of a list of animals that were taken from the central stock-yards in Drehem for offerings to several deities.</p>

<p><strong>2.2.</strong> The zabar-dab5 was an official whose name originally meant “the one who holds the mirror, lit. the bronze” (Foxvog 1980: 74; Steinkeller 1987). The zabar-dab5 played an important role in the texts that document deliveries of animals to the cults of the various deities that the state maintained in Nippur (Lafont 1983) and it has been suggested that the zabar-dab5 was the highest cultic official in the Ur III state (see Sallaberger 1999: 186-188 for details).</p>

<p><strong>2.3.</strong> The personal name i-sa3-ri2-ik is interesting in that it is a sandhi writing of a fairly common Semitic personal name issu-arik meaning ‘his arm is long’ (Hilgert 2002: 74; see p. 571 for a list of texts in which this orthography occurs).</p>

