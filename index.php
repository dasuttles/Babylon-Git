<h3 style="color: red;">From Babylon to Valdosta</h3>
<p>Dr. Richard Holmes Powell, first president of South Georgia State Normal College (now Valdosta State University), acquired ten Babylonian clay tablets for the school's students to study.  The tablets, which are roughly 1.5" square, are imprinted with Cuneiform writing and are indicative of daily record-keeping in ancient Mesopotamia. They were purchased in 1920 from archaeologist and antiquities dealer <a href="http://www.nationmaster.com/encyclopedia/Edgar-James-Banks">Edgar J. Banks</a> at a total cost of $40. Banks provided translations for each tablet, along with his personal guarantee regarding their authenticity.</p>

<p>Local legend has it that Powell purchased or arranged for the purchase of the tablets while he was on leave from the college serving abroad in the Red Cross in 1918. However, no evidence has yet been found to substantiate that Powell's Red Cross service was even overseas, or as to the connection between him and Banks.</p>
<div style="width: 25%; display: box; float: right; margin: 0px 0px 0px 10px;"><img style="position: relative; width: 100%;" src="img/container.png">
<span style="float: right; color: #333; font-size: x-small; margin: 0px 10px 1px 10px;">Tablets 9 and 10</span></div>
<p>The tablets were wrapped in tissue paper and shipped in a wooden box. Tablets numbered one through eight are in excellent condition; however, tablet number nine has been reduced to fragments and dust, and tablet number ten is very worn and fragile. A penciled notation on the box top reads, "#10 sundried, in poor condition," and in his letter, Banks notes that, "The sun dried tablets are seldom well preserved." It is unknown as to how tablet number nine was destroyed; either by shipping or after coming to the college.</p>

<p>Edgar J. Banks was a lesser-known archaeologist and did much of his work in the early 20<sup>th</sup> century. In 1900, Banks applied for and eventually received permission from Ottoman authorities to dig in the modern-day city of Bismya, Iraq, site of the ancient <a href="https://en.wikipedia.org/wiki/Adab_(city)">City of Adab</a>.<br>
<div style="width: 30%; display: box; float: left; margin: 5px 10px 0px 10px;">
<img style="position: relative; width: 100%;" src="img/banksateden.jpg"><br>
<span style="float: left; color: #333; font-size: x-small;">Edgar James Banks</span>
</div> 
It is likely that the tablets similar to the ones owned by VSU were acquired are from this area; however, he also purchased tablets from workers at other sites. Valdosta's tablets were primarily from the ancient Babylonian cities of Umma and Drehem. Many of Banks' Babylonian tablets are owned by academic institutions and museums across America, including The <a href="http://www.deseretnews.com/article/628586/Ancient-tablets-found-at-U---and-more-sought.html">University of Utah</a>, the <a href="https://www.lib.umn.edu/special">University of Minnesota</a>, <a href="http://libguides.sdstate.edu/content.php?pid=401094&sid=3284168">South Dakota State University</a> and the <a href="http://www.smm.org/anthropology/cuneiform">Science Museum of Minnesota</a>. According to Banks' biographer, <a href="http://www.ewas.us/" target="new">Ewa Wasilewska</a>, "he imported at least 11,000 such relics to the United States, and some estimates suggest the number may have been as many as 175,000 pieces." VSU is fortunate to be among the institutions that own a collection of Banks' Babylonian clay tablets.</p>

<h4>Exhibit</h4>
<p>The VSU Babylonian Clay Tablets are on permanent display in the Valdosta State University Archives and Special Collections, on the fourth floor of Odum Library<br><span style="font-size: x-small; font-style:italic;">Mon-Thur, 8:30 - 5:00 -- Fri, 8:30 - 3:00</span></p>

