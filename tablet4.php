<div class="translation">
    <p><strong>4.1.</strong> This document records Šulgi-irigu's receipt of the carcasses of four animals from Lugal-hegal. The Lugal-hegal attested here falls within the known career of a Lugal-hegal who was a "member of the ruling family of Umma" (Dahl 2003: 259) and seemed to have been involved in the care and feeding of working animals such as the onagers mentioned in this text (see Stepien 1996: 38 and 142-143). Although it is plausible that the Lugal-hegal in this text is the lugal-he2-gal2 dumu ur-nigargar mentioned by Dahl and Stepien, I have not yet been able to demonstrate this conclusively.</p>
