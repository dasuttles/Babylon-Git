<h2>Sun-Dried Table Number 9</h2>
    <p><strong>Information (from Banks letter):</strong> "Found at Senkereh, the Biblical Elassar mentioned in Genesis 14:1.  This is a sun dried first dynasty tablet or business contract, from the age of Hammurabi, King of Babylon about 2200 B.C. He was the Hammurabi of the Bible, a contemporary to Abraham.  This tablet is rare.  Few tablets from this dynasty are well preserved."</p>
    <h2>Sun-Dried Tablet 10</h2>
    <p><strong>Information (from Banks letter):</strong> "Found at Babylon.  This is a late or neo-Babylonian contract tablet or business document of the usual form.  It is sun-dried.  It is dated on the back, and the date bears the name of Nabonidus, King of Babylon from 555 to 538 B. C.   He was the last king of Babylon before the fall of the city to Cyrus.  The tablet was written sometime  between those two dates."</p>

