    	<h3>Valdosta Five</h3>
    	<blockquote>A rare messenger tablet (c. 2350 B.C.) on which an expense allowance for a temple messenger is
recorded. Bread, dates, oil, and wine are but a few of the provisions listed on the tablet.</blockquote>
        </div>
       
<div class="translation">
    <p><strong>5.1.</strong> This is a record of distributions to sukkals, 'court messengers/officials' and a ka-us2-sa2. sukkals regularly traveled far and wide, carrying out the business of the royal court, and in this text they receive the types of food that were the standard fare: beer and bread, fish and oil. The term naga, which is conventionally translated as 'alkali', has been dealt with by Butz (1984: 283-286), where he suggests several possible interpretations, including its use as soap or as a softening agent for the dried fish that often occur in messenger texts. Or, alternatively, naga may be a cover term for a variety of plant derivatives. Yoshikawa has explained gaba-aš "to the chest" as a shorthand that was used in Umma for trips to Elam, east of Mesopotamia (Yoshikawa 1988).</p>
